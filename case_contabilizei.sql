SELECT 
        DATA,
        LEADS,VENDAS,
        
        ROUND((VENDAS/LEADS)*100,2)
        AS TAXA_LEAD_PARA_VENDAS
FROM CONTABILIZEI_CASE
ORDER BY DATA;

--VALOR DO CAC(custo de aquisição por cliente)

SELECT 
        DATA,
        INVESTIMENTO,VENDAS,
        
        ROUND(INVESTIMENTO/VENDAS,2)
        AS INVESTIMENTO_P_CLIENTE
FROM CONTABILIZEI_CASE
ORDER BY DATA;

--EFCIENCIA DO VALOR INVESTIDO EM MERKETING

SELECT 
        DATA,
        INVESTIMENTO,LEADS,
        
        ROUND(INVESTIMENTO/LEADS,2)
        AS INVESTIMENTO_P_LEAD
FROM CONTABILIZEI_CASE
ORDER BY DATA;


